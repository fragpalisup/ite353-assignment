def get_minimum(t):
    return min(t)

def get_maximum(t):
    return max(t)

def get_sum(t):
    return sum(t)

sample_tuple = (5, 12, 3, 8, 20, 1)

print(f"Sample tuple: {sample_tuple}")
print(f"Minimum: {get_minimum(sample_tuple)}")
print(f"Maximum: {get_maximum(sample_tuple)}")
print(f"Sum: {get_sum(sample_tuple)}")